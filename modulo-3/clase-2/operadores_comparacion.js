//igualdad debil ==

console.log(5 == 5); //true

console.log(0 == ""); 

//igualdad fuerte ===
console.log(5 === 5); //true
console.log(0 === ""); //false

//Desigualdad debil !=
console.log(5 != 3); //true
console.log(undefined != null); 

//desigualdad fuerte !==
console.log(5 !== 5); //true
console.log(5 !== 5); //false

//mayor que >
console.log(7 > 5); //true

//menor que <
console.log(3 < 5); //true

//mayor o igual que >=
console.log(5 >= 5); //true

//menor o igual que <=
console.log(3 <= 5); //true 